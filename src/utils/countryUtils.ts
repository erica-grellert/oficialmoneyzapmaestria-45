export const getCountryByCode = (code: string): Country | undefined => {
  return countries.find((country) => country.code === code);
};

export const getCountryByDialCode = (dialCode: string): Country | undefined => {
  return countries.find((country) => country.dialCode === dialCode);
};

export const formatPhoneNumber = (
  country: Country,
  phoneNumber: string
): string => {
  // Remove all non-digit characters
  const cleanNumber = phoneNumber.replace(/\D/g, "");

  // If the number already starts with the country code, return as is
  if (cleanNumber.startsWith(country.dialCode)) {
    return cleanNumber;
  }

  // Add country code if not present
  return country.dialCode + cleanNumber;
};

export const parsePhoneNumber = (
  fullNumber: string
): { country: Country | null; localNumber: string } => {
  for (const country of countries) {
    if (fullNumber.startsWith(country.dialCode)) {
      const localNumber = fullNumber.substring(country.dialCode.length);
      return { country, localNumber };
    }
  }
  return { country: null, localNumber: fullNumber };
};
